package br.com.trackmycity.producers;

import java.util.ResourceBundle;

import javax.enterprise.inject.Produces;

import br.com.trackmycity.utils.ResourceBundleUtils;

public class ResourceBundleProducer {
	
	@Produces @Google
	public ResourceBundle getGoogleResourceBundle() {
		ResourceBundle googleResourceBundle = ResourceBundleUtils.getGoogleResourceBundle();
		return googleResourceBundle;
	}
}
